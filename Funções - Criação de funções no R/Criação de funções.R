#Cria a funcao media
#1. (Capo dos dados a serem inseridos na funcao)
#2. {acoes da funcao}
media = function(objeto){sum(objeto)/length(objeto)}

#OBS:Os dados a serem inseridos devem ter o formato de coluna para fazer a acao com os valores das linhas

#Teste 1
media(c(1,2,3))

#Teste 2
#Criacao de uma lista
alturas=c(1.4,1.7,2.0,1.6,1.8)
#Funcao usa o nome da lista como fonte de dados
media(alturas)
